#### Download data ####

# Unfortunately, the data cannot be downloaded automatically.
# Thus, you can take the following the steps to download the data.
# 1) First, visit https://dataverse.harvard.edu/dataset.xhtml?persistentId=doi:10.7910/DVN/BFEBHQ.
# 2) Then, press the 'Access Dataset' button.
# 3) Press "Download ZIP (384.2 KB).
# 4) Then, your browser should give you an option to save the dataset.
# 5) Unzip the folder, then you should be able to access the data.